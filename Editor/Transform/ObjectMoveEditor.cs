* @details ObjectMoveスクリプトを操作しやすいようにInspectorをカスタマイズしたもの
*/

using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEditor;

/**
* @brief   GameObjectTargetSelectorEditorを継承したObjectMoveEditor
*/

namespace ChUnity.Transform
{

    [CustomEditor(typeof(ObjectMove))]
    public class ObjectMoveEditor : Common.GameObjectTargetSelectorEditor
    {

        private void OnEnable()
        {
            targetObjectDescription = "移動するオブジェクト";
        }

        /**
       * @fn public void OnInspectorGUI()
       * @brief InspectorのGUIを変更する関数。
       */
        public override Object InspectorGUI()
        {

            var obj = base.InspectorGUI() as ObjectMove;

            obj.len  = InputField(obj.len,"移動速度");

            obj.normalizeFlg = IsTrueToggle(obj.normalizeFlg, "移動する際に正規化(ベクトルの長さを1にすること)を行うかのフラグ");

            obj.moveDirFlg = IsTrueToggle(obj.moveDirFlg, "移動する際に現在向いている方向を元に移動させるかのフラグ");

            Label("移動軸");

            obj.autoMoveDir = InputField(obj.autoMoveDir,"自動で移動する方向(大きさは実行時に1になります。)");

            return obj;

        }
    }

}
